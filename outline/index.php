			<h3 class="subPage-mainTitle">Organizer Comments</h3>
			<p class="mb10">Japan's largest and the only facility maintenance trade show:<br>"BUILDING MAINTENANCE &amp; CLEAN EXPO 2018" was held successfully from November 20th to 22th 2018 at Tokyo Big Sight, as being the first show ever among cooperation in between <a href="https://www.jma.or.jp/en/" target="_blank">Japan Management Association<i class="material-icons linkOut">filter_none</i></a> and <a href="http://www.j-bma.or.jp/jbma_eng/" target="_blank">Japan Building Maintenance Association<i class="material-icons linkOut">filter_none</i></a>.</p>
			<p class="mb10">As well as other industries, this sector with 3.8 trillion JPY market scale and 2.5 million working force, is also facing labor shortage and aging problems. Under such circumstance, automated cleaning robots were showing presences to experiment feasibilities for applying robotics in the cleaning field to complement human labor. In practice, the Chiba (A prefecture east of Tokyo) Station has implemented automated cleaning system already for streamlining the cleaning efficiency and cost.</p>
			<p>Cleaning robots function well on flat floors and have advantages to human hands in terms of efficiency and accuracy, where they memorize the geography of the building structures through mapping input made by human.</p>
